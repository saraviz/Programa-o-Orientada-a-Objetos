class MathUtils {
 
    static sum(n1, n2) {
      return n1 + n2;
    }

    static sub(n1, n2) {
        return n1 - n2;
    }

    static mul(n1, n2) {
        return n1 * n2;
    }

    static div(n1, n2) {
        return n1 / n2;
    }

  }
  
  console.log(MathUtils.sum(2, 5));
  console.log(MathUtils.sub(15, 2));
  console.log(MathUtils.mul(2, 15));
  console.log(MathUtils.div(8, 2));
